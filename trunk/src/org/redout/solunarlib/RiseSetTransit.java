package org.redout.solunarlib;

public class RiseSetTransit {
	private int body;
	private Double rise;
	private Double set;
	private Double transit;
	private int noState = 0;
	
	public int getNoState() {
		return noState;
	}
	public void setNoState(int noState) {
		this.noState = noState;
	}
	public int getBody() {
		return body;
	}
	public void setBody(int body) {
		this.body = body;
	}
	public Double getRise() {
		return rise;
	}
	public void setRise(Double rise) {
		this.rise = rise;
	}
	public Double getSet() {
		return set;
	}
	public void setSet(Double set) {
		this.set = set;
	}
	public Double getTransit() {
		return transit;
	}
	public void setTransit(Double transit) {
		this.transit = transit;
	}
	
	
}
