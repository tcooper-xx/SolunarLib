package org.redout.solunarlib;

public class UnderFoot {
	boolean isUnderFoot;
	double underfootTime;
	
	public boolean isUnderFoot() {
		return isUnderFoot;
	}
	public void setUnderFoot(boolean isUnderFoot) {
		this.isUnderFoot = isUnderFoot;
	}
	public double getUnderfootTime() {
		return underfootTime;
	}
	public void setUnderfootTime(double underfootTime) {
		this.underfootTime = underfootTime;
	}
	
	

}
