package org.redout.solunarlib;

public class Quad {
	double xe;
	double ye;
	double z1;
	double z2;
	int nz;
	
	public int getNz() {
		return nz;
	}
	public void setNz(int nz) {
		this.nz = nz;
	}
	public double getXe() {
		return xe;
	}
	public void setXe(double xe) {
		this.xe = xe;
	}
	public double getYe() {
		return ye;
	}
	public void setYe(double ye) {
		this.ye = ye;
	}
	public double getZ1() {
		return z1;
	}
	public void setZ1(double z1) {
		this.z1 = z1;
	}
	public double getZ2() {
		return z2;
	}
	public void setZ2(double z2) {
		this.z2 = z2;
	}
}
